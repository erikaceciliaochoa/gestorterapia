            int dia=fecha.getDayOfTheWeek(this.jCalendar.getDate());
            String diaSemana=fecha.getStringDiaSemana(dia);
            
            //cargarComboHorario
//            this.cboHorario.removeAllItems();
//            List<Object[]> lista =horarioDAO.getHorariosXdia(diaSemana);
//            for(int i=0; i< lista.size(); i++){
