        Motivo motivo=AbstractDAO.getEntidad(idMot, Motivo.class);
        Prueba prueba=AbstractDAO.getEntidad(idPru, Prueba.class);
        
        Tratamiento tratamiento=AbstractDAO.getEntidad(idTrat, Tratamiento.class);
        Diagnostico diagnostico=AbstractDAO.getEntidad(idDiag, Diagnostico.class);
        
        HorarioCab hrCab=AbstractDAO.getEntidad(idHrCab, HorarioCab.class);
        Calendar fechaSolicitud=fecha.retornarCalendar(this.txtFechaSolicitud.getText());
        Calendar fechaTurno=fecha.retornarCalendar(this.txtFechaTurno.getText());
        String horaSolicitud=this.txtHoraSolicitud.getText();
        String horaTurno=this.txtHoraTurno.getText();
        String observaciones=this.txtAreaObservaciones.getText();
        long monto=Long.parseLong(this.txtMonto.getText());
        turno.setFecha(fechaTurno);
        turno.setFechaSolicitud(fechaSolicitud);
        turno.setHora(horaTurno);
        turno.setHoraSolicitud(horaSolicitud);
        turno.setPaciente(paciente);
        turno.setMotivo(motivo);
        turno.setPrueba(prueba);
        //Tratamiento, Diagnostico
        turno.setTratamiento(tratamiento);
        turno.setDiagnostico(diagnostico);
        turno.setHorario(hrCab);
        turno.setObservaciones(observaciones);
        turno.setMonto(monto);
