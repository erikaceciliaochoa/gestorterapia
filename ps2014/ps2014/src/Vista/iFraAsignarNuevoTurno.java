    private void btnOkHrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkHrActionPerformed
        //!existeTurno()
        fecha = new Fecha();
        String fechaElegida = fecha.getFormatedFechaToCalendar(this.txtFechaSelec.getText());
        String horarioElegido = this.cboHorario.getSelectedItem().toString();
                
        turnoDAO = new TurnoDAO();
        Turno cant=turnoDAO.getCantTurno(fechaElegida, horarioElegido);
        if(cant==null){          
            if(!cboHorario.getSelectedItem().equals("No hay horarios.")){
                //this.cboHorario.addItem("No hay horarios.");
                this.txtFechaTurno.setText(this.txtFechaSelec.getText());
                this.txtHoraTurno.setText(this.cboHorario.getSelectedItem().toString());

                recuperarIdHorario();
                JOptionPane.showMessageDialog(this, "<html><font size=5>Turno correctamente seleccionado."); 
            }
            else{
                JOptionPane.showMessageDialog(this, "<html><font size=5>No hay horarios dísponible para esta fecha.");
            }       
        }
        else{
            //borrar el panel de datosPaciente!!
            clearForm(panelDatosHr);
            String paciente=cant.getPaciente().getApellido() + ", " + cant.getPaciente().getNombre();
            JOptionPane.showMessageDialog(this, "<html><font size=5>Este turno fue asignado a: " + paciente);          
        }
    }//GEN-LAST:event_btnOkHrActionPerformed

    
