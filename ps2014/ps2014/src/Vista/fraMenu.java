                // Carga el fichero de ayuda
//              File fichero = new File("../help/help_set.hs");
                File fichero = new File("./help/help_set.hs");                
        //      File fichero = new File("C:\\Users\\usuario\\Desktop\\TECNICATURA 2014\\Ejemplo-JavaHelp-1.0/help/help_set.hs");
