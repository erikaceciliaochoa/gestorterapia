  public boolean validar(){
    
    boolean res=true; String mnsj, tit;
    tit="Atenci칩n";    
    if((txtUsuario.getText().trim()).length()==0)
    {   mnsj="<html><h2 style=color:red;>Usuario. Dato Requerido.</h2></html>";
        
        JOptionPane.showMessageDialog(this, mnsj,tit,JOptionPane.WARNING_MESSAGE);
        txtUsuario.grabFocus();
        return false;
    }

    if((ptxtPass.getText().trim()).length()==0)
    {   mnsj="<html><h2 style=color:red;>Clave. Dato Requerido.</h2></html>";
        JOptionPane.showMessageDialog(this, mnsj,tit,JOptionPane.WARNING_MESSAGE);
        
        ptxtPass.grabFocus();
        return false;
    }
    
    return res;
}
  
