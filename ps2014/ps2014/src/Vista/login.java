  class TimerListener implements ActionListener{
      
      @Override
      public void actionPerformed(ActionEvent e){
          cont++;
          barrita.setValue(cont);
          if(cont==100){
              tiempo.stop();
              esconder();
              fraMenu mnu=new fraMenu();
              mnu.setVisible(true);
              setVisible(false);
//              this.dispose();
          } 
//          throw new UnsupportedOperationException("Not supported yet.");
      }
  }
  
  public void esconder(){this.setVisible(false);}
  public void activar(){tiempo.start();}
  
    public void inicioSesion(){
        String usu=txtUsuario.getText();
        String pass=String.valueOf(ptxtPass.getPassword());
        
        if(usu.equals("erika") && pass.compareTo("1234")==0){
            barrita.setVisible(true);    
            cont=1;
            barrita.setValue(0);
            barrita.setStringPainted(true);   
            tiempo= new Timer(TWO_SECOND,new TimerListener());
            activar();
            
            AbstractDAO.getEntidad(1, Usuario.class);
               
        } else{
               String mensaje="<html><h2 style=color:red;>Error al ingresar, usuario o contrase침a incorrectas.</h2></html>";
               JOptionPane.showMessageDialog(this, mensaje, "ERROR",JOptionPane.ERROR_MESSAGE);           
           }
    }

