public boolean validar(){
    String tit="Atención"; String msj; 
    boolean res=true;
        
    if(this.txtUsu.getText().isEmpty()){
        msj="<html><font size=5 style=color:red;>Nombre de Usuario. Dato Requerido. ";
        JOptionPane.showMessageDialog(this, msj,tit,JOptionPane.WARNING_MESSAGE);
        txtUsu.grabFocus();
        return false;
    }

    if(this.ptxtPass.getPassword().length==0)
    {   msj="<html><font size=5 style=color:red;>Clave de Usuario. Dato Requerido. ";
        JOptionPane.showMessageDialog(this, msj,tit,JOptionPane.WARNING_MESSAGE);
        ptxtPass.grabFocus();
        return false;
    }
    
    boolean op = conexion.getInstance().verificarUsuario(txtUsu.getText(), ptxtPass.getText());
    if (op){ 
         msj="<html><font size=5 style=color:red;>Ya existe un usuario con estos datos.";
         JOptionPane.showMessageDialog(this, msj,tit,JOptionPane.WARNING_MESSAGE);
         txtUsu.grabFocus();
         return false;
    }
    
    return res;
}

