private void arranque(){
    habilitarCampos(panelDatos,false);
    limpiarCampos(panelDatos);
    this.btnGuardar.setEnabled(false);
    this.btnNuevo.setEnabled(true);
    this.btnGuardar.setText("Guardar");
}
    
public void guardar(){
    setearEstado(panelDatos);
    arranque();
}

public void nuevo(){
    this.btnNuevo.setEnabled(false);
    this.btnGuardar.setEnabled(true);
    habilitarCampos(panelDatos,true);
    cargarChk(panelDatos);
}

public void habilitarCampos(JPanel jPanel, boolean op){
    for (Component component : jPanel.getComponents()) {
        if (component instanceof JCheckBox) {
                ((JCheckBox) component).setEnabled(op);
        } 
    }      
}

public void limpiarCampos(JPanel jPanel){
    for (Component component : jPanel.getComponents()) {
        //Limpia todos los JTextField de un JPanel
        if (component instanceof JCheckBox) {
                ((JCheckBox) component).setSelected(false);
        } 
    }      
}

public void setearEstado(JPanel jPanel){   
    for (Component component : jPanel.getComponents()) {
        if (component instanceof JCheckBox) {
            for (int j = 0; j < numChk; j++) { 
                if(((JCheckBox) component).getText().equals(DIAS[j])){
                    int id=Integer.parseInt(((JCheckBox) component).getName());
                    //Almacenar en la BD
                    DiaLaboral dl = new DiaLaboral();
                    dl.setId(id);
                    //dl.setDes(((JCheckBox) component).getText());
                    dl.setDes(DIAS[j]);
                    if(((JCheckBox) component).isSelected()){
                        dl.setEstatus(DiaLaboral.Estatus.ACTIVO);}
                    else{dl.setEstatus(DiaLaboral.Estatus.INACTIVO);}
                    AbstractDAO.almacenaEntidad(dl);
                }
           }                
        } 
    } 
}
    
public void cargarChk(JPanel jPanel){   
    for (Component component : jPanel.getComponents()) {
        if (component instanceof JCheckBox) {
            for (int j = 0; j < numChk; j++) { 
                if(((JCheckBox) component).getText().equals(DIAS[j])){
                    int id=Integer.parseInt(((JCheckBox) component).getName());
                    List<DiaLaboral> lista=AbstractDAO.getListaEntidadesXid(DiaLaboral.class, id);                    
                    for (DiaLaboral dl : lista) {
                        if(dl.getEstatus()==DiaLaboral.Estatus.ACTIVO)
                            ((JCheckBox) component).setSelected(true);
                        else
                            ((JCheckBox) component).setSelected(false);    
                    }
                }
           }                
        } 
    } 
}  




