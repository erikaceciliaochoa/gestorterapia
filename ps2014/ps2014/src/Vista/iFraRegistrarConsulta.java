    public void cargarTabla(int op){
        boolean ban=true;
        tableModelPac();
        boolean asistio,pagado,cancelado;
        turnoDAO = new TurnoDAO();
        List<Turno> lista = null;
        if (op==1){ 
            String FechaSelec=fecha.getStringXfecha(this.calendarioTurnos.getDate());
            String fechaElegida = fecha.getFormatedFechaToCalendar(FechaSelec);

            lista=turnoDAO.getTurnoXfecha(fechaElegida);
            ban=false;
        } else if(op==3){
//            fecha=new Fecha();
            String FechaSelec=fecha.getStringXfecha(txtPorFecha.getDate());
            String fechaElegida = fecha.getFormatedFechaToCalendar(FechaSelec);

            lista=turnoDAO.getTurnoXfecha(fechaElegida);              
        } else if(op==4){
//            fecha=new Fecha();
//            Calendar fechaNac = fecha.retornarCalendar(fecha.getStringXfecha(txtFechaNac.getDate()));
            String f1=fecha.getFormatedFechaToCalendar(fecha.getStringXfecha(txtFDesde.getDate()));
            String f2=fecha.getFormatedFechaToCalendar(fecha.getStringXfecha(txtFHasta.getDate())); 
//            JOptionPane.showMessageDialog(this, f1+"-"+f2);
            lista = turnoDAO.getTurnoXRangofecha(f1, f2);
        }
        
        if(!lista.isEmpty() ){
            for (Iterator<Turno> it = lista.iterator(); it.hasNext();) {
                Turno t = it.next();

                if(t.getEstatusAsistio()==Turno.EstatusAsistio.ASISTIO) asistio=true;
                    else asistio=false;
                if(t.getEstatusPago()==Turno.EstatusPago.ABONADO) pagado=true;
                    else pagado=false;         
                if(t.getEstatusTurno()==Turno.EstatusTurno.INACTIVO) cancelado=true;
                    else cancelado=false;

                String fTurno = fecha.retornarString(t.getFecha());

                model.addRow(new Object[]{
                    t.getNum(), 
                    t.getPaciente().getApellido() + ", " + t.getPaciente().getNombre(),  
                    fTurno+ " - " + t.getHora(), 
                    asistio,
                    pagado,
                    cancelado
                });
            }
        }else if(ban){
            JOptionPane.showMessageDialog(this, "<html><font size=5>Esta b√∫squeda no arrojo resultados.");
        }
    }
    
