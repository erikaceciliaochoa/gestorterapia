        String diag, trat; //diag=trat="";
        try{
            diag=this.cboDiagnostico.getSelectedItem().toString();
            Diagnostico d=turnoDAO.getDiagnostico(diag);          
            t.setDiagnostico(d);            
        } catch(Exception e){}
        
        try{
            trat=this.cboTratamiento.getSelectedItem().toString();        
            Tratamiento tr=turnoDAO.getTratamiento(trat);
            t.setTratamiento(tr);
        } catch(Exception e){}
        
