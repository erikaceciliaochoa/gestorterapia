    private void jTabbedPane1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabbedPane1MousePressed
        int op=0;
        if(this.jTabbedPane1.getSelectedIndex()<2)op=1;//Es paciente y horario
        if(this.jTabbedPane1.getSelectedIndex()==2 && btnSelect.isEnabled() && btnEditar.isEnabled()) op=2;//Motivo
        if(this.jTabbedPane1.getSelectedIndex()==3 && btnSelect.isEnabled() && btnEditar.isEnabled())op=3;//Prueba
        if(this.jTabbedPane1.getSelectedIndex()==4 && btnSelect.isEnabled() && btnEditar.isEnabled())op=4;//Diagn√≥stico
        if(this.jTabbedPane1.getSelectedIndex()==5 && btnSelect.isEnabled() && btnEditar.isEnabled())op=5;//Tratamiento
              
        switch(op){
            case 1:
                iFraRegistrarConsulta.btnSelect.setVisible(false);
                break;
            case 2:
                resetearTablaMotivo();
                iFraRegistrarConsulta.btnSelect.setText("Seleccionar Motivo");
                icon = createImageIcon("img/_select.png");
                iFraRegistrarConsulta.btnSelect.setIcon(icon);
                iFraRegistrarConsulta.btnSelect.grabFocus();
                iFraRegistrarConsulta.btnSelect.setVisible(true);
                break;
            case 3:
                resetearTablaPrueba();
                iFraRegistrarConsulta.btnSelect.setText("Seleccionar Prueba");
                icon = createImageIcon("img/_select.png");
                iFraRegistrarConsulta.btnSelect.setIcon(icon);
                iFraRegistrarConsulta.btnSelect.grabFocus();
                iFraRegistrarConsulta.btnSelect.setVisible(true);
                break;
            case 4:
                if(this.cboDiagnostico.getSelectedIndex()!=-1 && diagnostico){
                    iFraRegistrarConsulta.btnSelect.setText("Cancelar Ingreso");
                    icon = createImageIcon("img/cancel.png");
                    iFraRegistrarConsulta.btnSelect.setIcon(icon);
                    break;
                }
                else{
                    iFraRegistrarConsulta.btnSelect.setText("Ingrese Diagn√≥stico");
                    icon = createImageIcon("img/selection-input2.png");
                    iFraRegistrarConsulta.btnSelect.setIcon(icon);
                    iFraRegistrarConsulta.btnSelect.grabFocus();
                    iFraRegistrarConsulta.btnSelect.setVisible(true);
                    iFraRegistrarConsulta.btnSelect.setEnabled(true);
//                    this.cboDiagnostico.setEnabled(false);
//                    this.cboDiagnostico.setSelectedIndex(-1);
//                    txtDescDiag.setEnabled(false);
//                    txtDescDiag.setText(null);
                }
                //selection-input2.png
//                EnableForm(panelDatosDiag, true);
                break;
            case 5:
                if((this.cboTratamiento.getSelectedIndex()!=-1) && tratamiento){
                    iFraRegistrarConsulta.btnSelect.setText("Cancelar Ingreso");
                    icon = createImageIcon("img/cancel.png");
                    iFraRegistrarConsulta.btnSelect.setIcon(icon);
                    break;
                }
                else{
                    iFraRegistrarConsulta.btnSelect.setText("Ingrese Tratamiento");
                    icon = createImageIcon("img/select-input.png");
                    iFraRegistrarConsulta.btnSelect.setIcon(icon);
                    iFraRegistrarConsulta.btnSelect.grabFocus();
                    iFraRegistrarConsulta.btnSelect.setVisible(true);
                    iFraRegistrarConsulta.btnSelect.setEnabled(true);
//                    this.cboTratamiento.setEnabled(false);
//                    this.cboTratamiento.setSelectedIndex(-1);
//                    txtDescTrat.setEnabled(false);
//                    txtDescTrat.setText(null);
                }
//                EnableForm(panelDatosTrat, true);
                break;
            default:
                iFraRegistrarConsulta.btnSelect.setVisible(false); //QUITAR CASE 1
                break;
        }
    }//GEN-LAST:event_jTabbedPane1MousePressed

